def make_row(idx: int, text_pair: tuple, cls: int) -> dict:
    dev, rom = text_pair
    if cls == 0:
        hs = random.randint(2, 3)
        ol = random.randint(0, 1)
        ni = 0
    elif cls == 1:
        hs = random.randint(0, 1)
        ol = random.randint(2, 3)
        ni = 0
    else:
        hs = 0
        ol = 0
        ni = random.randint(3, 6)
    return {
        "index":              idx,
        "count":              hs + ol + ni,
        "hate_speech":        hs,
        "offensive_language": ol,
        "neither":            ni,
        "class":              cls,
        "tweet_devanagari":   dev,
        "tweet_romanized":    rom,
    }


def build_dataset(target_size: int = 10000) -> list:
    n_hate   = int(target_size * 0.06)
    n_offend = int(target_size * 0.77)
    n_clean  = target_size - n_hate - n_offend

    rows = []
    idx = 0
    for cls, pool, n in [
        (0, HATE_SPEECH,       n_hate),
        (1, OFFENSIVE_LANGUAGE, n_offend),
        (2, NEITHER,            n_clean),
    ]:
        for _ in range(n):
            rows.append(make_row(idx, random.choice(pool), cls))
            idx += 1

    random.shuffle(rows)
    for i, r in enumerate(rows):
        r["index"] = i
    return rows


def write_csv(rows: list, path: str, columns: list):
    with open(path, "w", newline="", encoding="utf-8-sig") as f:
        writer = csv.DictWriter(f, fieldnames=columns, extrasaction="ignore")
        writer.writeheader()
        writer.writerows(rows)
    print(f"  ✓  {path}  ({len(rows):,} rows)")


